public class Case {
    private String caseNumber;
    private String caseIssue;
    private String caseDesc;
    private String caseDecision;
    private String caseStatus;

    public String getCaseNumber() {
        return caseNumber;
    }

    public void setCaseNumber(String caseNumber) {
        this.caseNumber = caseNumber;
    }

    public String getCaseIssue() {
        return caseIssue;
    }

    public void setCaseIssue(String caseIssue) {
        this.caseIssue = caseIssue;
    }

    public String getCaseDesc() {
        return caseDesc;
    }

    public void setCaseDesc(String caseDesc) {
        this.caseDesc = caseDesc;
    }

    public String getCaseDecision() {
        return caseDecision;
    }

    public void setCaseDecision(String caseDecision) {
        this.caseDecision = caseDecision;
    }

    public String getCaseStatus() {
        return caseStatus;
    }

    public void setCaseStatus(String caseStatus) {
        this.caseStatus = caseStatus;
    }
    
    
            
    
}
